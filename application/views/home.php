<script>
    window.onload = function () {
        /* Load search box */
        $(".search").select2({
            placeholder: 'Cherchez votre université',
            width: '100%',
            allowClear: true,
            multiple: false,
            theme: "classic",
            ajax: {
                url: window.location.href + 'facdata',
                dataType: 'json',
                type: "GET",
                delay: 250,
                data: function (term) {
                    return {
                        q: term
                    };
                },
                results: function (data) {
                    return {
                        results: $.map(data, function (item) {
                            return {
                                id: item.idEcole,
                                text: item.NomEcole,
                                image: item.Image,
                                email: item.AdEmail,
                                slug: item.slug
                            }
                        })
                    };
                }
            },
            cache: true
        });

        /* Fill from misc data */
        $.ajax({
            url: window.location.href + 'data',
            dataType: 'json',
            success: function (data) {
                $('.nbusers').contents().filter(function () {
                    this.textContent = this.textContent.replace('x', data[0].nbusers);
                });
                $('.nbcours').contents().filter(function () {
                    this.textContent = this.textContent.replace('x', data[0].nbcours);
                    this.textContent = this.textContent.replace('y', data[0].nbfacs);
                })
            }
        });

        /* Submit search */
        $(".search-button").on('click', function (e) {
            var result = window.location.href + 'cours#' + $('.search').select2('data').slug;
            window.location.href = result;
        });

        /* Scroll top */
        $('.scroll-top').on('click', function () {
            $("html, body").animate(
                {
                    scrollTop: 0
                }, "slow");
        });
    }
</script>
